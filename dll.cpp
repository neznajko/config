namespace dll {
ostream& operator <<( ostream& _, const Setoff& setoff ){
    _ << "{";
    if( !setoff.empty()) {
        _ << " ";
        ofst_t k = setoff.fyorst();
        do {
            _ << Board::get_coord( k ) << " ";
            k = setoff.next( k );
        } while( k );
    }
    return _ << "}";
}}
