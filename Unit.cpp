void Unit::tcpdump() const {
    if( not_nil()){
        cout << ch() 
             << fig->coord() 
             << ": "
             << ( const Figure* ) fig
             << endl
             << "     "
             << fig->get_sq()
             << endl; 
    } else {
        cout << "NIL\n";
    }
}
