std::random_device                                    Hash::dev;
std::uniform_int_distribution <u64_t>   Hash::dist( 0, U64MAX );
std::mt19937_64                                       Hash::gen;
vector <vector <u64_t>>                Hash::_board( Board::SIZ, 
                                    vector <u64_t> ( VOID, 0 ));
u64_t                                         Hash::_the_switch;
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
void Hash::initialize( u64_t seed )
{
    gen.seed( seed );
    _the_switch = genu64();
    for( int i = 0; i < Board::SIZ; ++i ){
        for( int j = 0; j < VOID; ++j ){
            _board[ i ][ j ] = genu64();
        }
    }
}
